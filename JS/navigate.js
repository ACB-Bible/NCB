function ncbSpanHover(id) {
    if (spanHover) {
        document.getElementById(id).style = "border-top: 5px solid white;";
        spanHover = false;
    } else {
        document.getElementById(id).style = "border-top: 5px solid #025c8d;";
        spanHover = true;
    };
};

